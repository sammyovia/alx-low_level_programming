#include "main.h"
#include <stdio.h>
/**
 * print_to_98 - prints all natural no. from n - 98
 * Return: 0 Always
 * @n: input value
 * Description: prints all natural numbers from to 98
 */
void print_to_98(int n)
{
	if (n < 98)
	{
		while (n <= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n++;
		}
	}
	else if (n > 98)
	{
		while (n >= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n--;
		}
	}
	else
	{
		printf("98");
	}
	printf("\n");
}

